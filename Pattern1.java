public class Pattern1 {
    public static void main(String[] args) {

        int n=6;

        for(int row=1;row<=5;row++)
        {
            for(int col=1;col<=n-row;col++)
            {
                System.out.print("  *   ");

            }
            {
                System.out.println();
            }
    }
}
}


